const fs = require('fs')
const path = require('path')
const { execSync } = require('child_process')

const BASE_URL = 'https://aicalc.cloud'
const OUTPUT_DIR = path.join(__dirname, '..', 'llm-pricing')
const TMP_FILE = path.join(__dirname, '_tmp_llmpricing_data.mjs')

const dataFile = path.join(__dirname, '..', 'src', 'llm-pricing', 'seo', 'pricing-seo-data.ts')

fs.writeFileSync(TMP_FILE, `
import { PRICING_SEO_PAGES } from '${dataFile.replace(/\\/g, '/')}';
import { writeFileSync, mkdirSync } from 'fs';
import { join, resolve } from 'path';

const OUTPUT_DIR = '${OUTPUT_DIR.replace(/\\/g, '/')}';
const BASE_URL = '${BASE_URL}';
const SCRIPTS_DIR = '${__dirname.replace(/\\/g, '/')}';

function escapeHtml(s) {
  return s.replace(/&/g, '&amp;').replace(/"/g, '&quot;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
}

const inputs = [];
console.log('Found ' + PRICING_SEO_PAGES.length + ' llm-pricing SEO pages');

for (const page of PRICING_SEO_PAGES) {
  const dir = join(OUTPUT_DIR, page.slug);
  mkdirSync(dir, { recursive: true });

  const modelName = page.h1.replace(' API Pricing', '');

  // Use actual FAQ data from pricing-seo-data.ts for rich JSON-LD answers
  const faqSchema = (page.faq || []).map(item => ({
    "@type": "Question",
    "name": item.q,
    "acceptedAnswer": {
      "@type": "Answer",
      "text": item.a
    }
  }));

  // BreadcrumbList schema
  const breadcrumbSchema = {
    "@context": "https://schema.org",
    "@type": "BreadcrumbList",
    "itemListElement": [
      { "@type": "ListItem", "position": 1, "name": "Home", "item": BASE_URL },
      { "@type": "ListItem", "position": 2, "name": "LLM Pricing", "item": BASE_URL + "/llm-pricing/" },
      { "@type": "ListItem", "position": 3, "name": page.h1, "item": BASE_URL + "/llm-pricing/" + page.slug + "/" },
    ]
  };

  // SoftwareApplication schema
  const softwareSchema = {
    "@context": "https://schema.org",
    "@type": "SoftwareApplication",
    "name": modelName,
    "applicationCategory": "DeveloperApplication",
    "operatingSystem": "Cloud API",
    "offers": {
      "@type": "Offer",
      "price": "0",
      "priceCurrency": "USD",
      "description": page.description
    }
  };

  const html = \`<!doctype html>
<html lang="en">
  <head>
    <meta charset="UTF-8" />
    <link rel="icon" type="image/svg+xml" href="/favicon.svg" />
    <meta name="viewport" content="width=device-width, initial-scale=1.0" />
    <title>\${escapeHtml(page.title)}</title>
    <meta name="description" content="\${escapeHtml(page.description)}" />
    <meta name="keywords" content="\${escapeHtml(page.keywords.join(', '))}" />
    <link rel="canonical" href="\${BASE_URL}/llm-pricing/\${page.slug}/" />
    <meta property="og:title" content="\${escapeHtml(page.title)}" />
    <meta property="og:description" content="\${escapeHtml(page.description)}" />
    <meta property="og:type" content="article" />
    <meta property="og:url" content="\${BASE_URL}/llm-pricing/\${page.slug}/" />
    <meta property="og:image" content="\${BASE_URL}/og-image.png" />
    <meta name="twitter:card" content="summary_large_image" />
    <meta name="twitter:image" content="\${BASE_URL}/og-image.png" />
    <meta name="twitter:title" content="\${escapeHtml(page.title)}" />
    <meta name="twitter:description" content="\${escapeHtml(page.description)}" />
    <link rel="preconnect" href="https://fonts.googleapis.com" />
    <link rel="preconnect" href="https://fonts.gstatic.com" crossorigin />
    <link href="https://fonts.googleapis.com/css2?family=Inter:wght@400;500;600;700&family=JetBrains+Mono:wght@400;500;600;700&display=swap" rel="stylesheet" />
    <script type="application/ld+json">
    {
      "@context": "https://schema.org",
      "@type": "FAQPage",
      "mainEntity": \${JSON.stringify(faqSchema)}
    }
    </script>
    <script type="application/ld+json">
    \${JSON.stringify(breadcrumbSchema)}
    </script>
    <script type="application/ld+json">
    \${JSON.stringify(softwareSchema)}
    </script>
  </head>
  <body>
    <script type="application/json" id="seo-data">\${JSON.stringify(page)}</script>
    <div id="root"></div>
    <script type="module" src="/src/llm-pricing/seo-main.tsx"></script>
  </body>
</html>\`;

  writeFileSync(join(dir, 'index.html'), html);
  inputs.push("  'pricing-lt-" + page.slug + "': resolve('" + SCRIPTS_DIR + "/..', 'llm-pricing/" + page.slug + "/index.html')");
}

const fragment = \`// Auto-generated by scripts/generate-llm-pricing-html.cjs
// Do not edit manually. Run: node scripts/generate-llm-pricing-html.cjs
const { resolve } = require('path')
module.exports = {
\${inputs.join(',\\n')}
}
\`;
writeFileSync(join(SCRIPTS_DIR, 'vite-llm-pricing-inputs.cjs'), fragment);
console.log('Generated ' + PRICING_SEO_PAGES.length + ' llm-pricing HTML files');
console.log('Wrote vite inputs to scripts/vite-llm-pricing-inputs.cjs');
`)

try {
  execSync(`npx tsx "${TMP_FILE}"`, { stdio: 'inherit', cwd: path.join(__dirname, '..') })
} finally {
  fs.unlinkSync(TMP_FILE)
}
