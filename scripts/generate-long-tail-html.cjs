const fs = require('fs')
const path = require('path')
const { execSync } = require('child_process')

const BASE_URL = 'https://aicalc.cloud'
const OUTPUT_DIR = path.join(__dirname, '..', 'cron-generator')
const TMP_FILE = path.join(__dirname, '_tmp_data.mjs')

// Use esbuild to transpile TS to JS, then import
const dataFile = path.join(__dirname, '..', 'src', 'cron', 'seo', 'long-tail-data.ts')

// Build a temporary ESM wrapper that re-exports the array
fs.writeFileSync(TMP_FILE, `
import { LONG_TAIL_PAGES } from '${dataFile.replace(/\\/g, '/')}';
import { writeFileSync, mkdirSync } from 'fs';
import { join, resolve } from 'path';

const OUTPUT_DIR = '${OUTPUT_DIR.replace(/\\/g, '/')}';
const BASE_URL = '${BASE_URL}';
const SCRIPTS_DIR = '${__dirname.replace(/\\/g, '/')}';

function escapeHtml(s) {
  return s.replace(/&/g, '&amp;').replace(/"/g, '&quot;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
}

const LOCALES = ['zh', 'ja', 'es', 'pt', 'fr', 'de', 'ko'];
const inputs = [];
console.log('Found ' + LONG_TAIL_PAGES.length + ' long-tail pages');

for (const page of LONG_TAIL_PAGES) {
  const dir = join(OUTPUT_DIR, page.slug);
  mkdirSync(dir, { recursive: true });

  const faqSchema = page.faq.map(f => ({
    "@type": "Question",
    "name": f.q,
    "acceptedAnswer": { "@type": "Answer", "text": f.a }
  }));

  const howToSchema = {
    "@context": "https://schema.org",
    "@type": "HowTo",
    "name": "How to set up " + page.h1,
    "description": page.description,
    "step": [
      { "@type": "HowToStep", "text": "Open your crontab editor: run crontab -e in your terminal." },
      { "@type": "HowToStep", "text": "Add the line: " + page.cron + " /path/to/your/script.sh" },
      { "@type": "HowToStep", "text": "Save and exit. Verify with crontab -l." }
    ]
  };

  const html = \`<!doctype html>
<html lang="en">
  <head>
    <meta charset="UTF-8" />
    <link rel="icon" type="image/svg+xml" href="/favicon.svg" />
    <meta name="viewport" content="width=device-width, initial-scale=1.0" />
    <title>\${escapeHtml(page.title)}</title>
    <meta name="description" content="\${escapeHtml(page.description)}" />
    <meta name="keywords" content="\${escapeHtml(page.keywords.join(', '))}" />
    <link rel="canonical" href="\${BASE_URL}/cron-generator/\${page.slug}/" />
    <meta property="og:title" content="\${escapeHtml(page.title)}" />
    <meta property="og:description" content="\${escapeHtml(page.description)}" />
    <meta property="og:type" content="article" />
    <meta property="og:url" content="\${BASE_URL}/cron-generator/\${page.slug}/" />
    <meta name="twitter:card" content="summary" />
    <meta name="twitter:title" content="\${escapeHtml(page.title)}" />
    <meta name="twitter:description" content="\${escapeHtml(page.description)}" />
    <link rel="preconnect" href="https://fonts.googleapis.com" />
    <link rel="preconnect" href="https://fonts.gstatic.com" crossorigin />
    <link href="https://fonts.googleapis.com/css2?family=Inter:wght@400;500;600;700&family=JetBrains+Mono:wght@400;500;600;700&display=swap" rel="stylesheet" />
    <script type="application/ld+json">
    {
      "@context": "https://schema.org",
      "@type": "FAQPage",
      "mainEntity": \${JSON.stringify(faqSchema)}
    }
    </script>
    <script type="application/ld+json">
    \${JSON.stringify(howToSchema)}
    </script>
  </head>
  <body>
    <script type="application/json" id="seo-data">\${JSON.stringify(page)}</script>
    <div id="root"></div>
    <script type="module" src="/src/cron/long-tail-main.tsx"></script>
  </body>
</html>\`;

  writeFileSync(join(dir, 'index.html'), html);
  inputs.push("  'cron-lt-" + page.slug + "': resolve('" + SCRIPTS_DIR + "/..', 'cron-generator/" + page.slug + "/index.html')");
}

const fragment = \`// Auto-generated by scripts/generate-long-tail-html.cjs
// Do not edit manually. Run: node scripts/generate-long-tail-html.cjs
const { resolve } = require('path')
module.exports = {
\${inputs.join(',\\n')}
}
\`;
writeFileSync(join(SCRIPTS_DIR, 'vite-lt-inputs.cjs'), fragment);
console.log('Generated ' + LONG_TAIL_PAGES.length + ' HTML files');
console.log('Wrote vite inputs to scripts/vite-lt-inputs.cjs');
`)

// Run the temp script using the project's node with ts support via tsx
try {
  execSync(`npx tsx "${TMP_FILE}"`, { stdio: 'inherit', cwd: path.join(__dirname, '..') })
} finally {
  fs.unlinkSync(TMP_FILE)
}
