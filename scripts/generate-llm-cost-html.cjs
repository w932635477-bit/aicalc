const fs = require('fs')
const path = require('path')
const { execSync } = require('child_process')

const BASE_URL = 'https://aicalc.cloud'
const OUTPUT_DIR = path.join(__dirname, '..', 'llm-pricing')
const TMP_FILE = path.join(__dirname, '_tmp_llmcost_data.mjs')

const dataFile = path.join(__dirname, '..', 'src', 'llm-pricing', 'seo', 'cost-seo-data.ts')

fs.writeFileSync(TMP_FILE, `
import { COST_SEO_PAGES } from '${dataFile.replace(/\\/g, '/')}';
import { writeFileSync, mkdirSync } from 'fs';
import { join, resolve } from 'path';

const OUTPUT_DIR = '${OUTPUT_DIR.replace(/\\/g, '/')}';
const BASE_URL = '${BASE_URL}';
const SCRIPTS_DIR = '${__dirname.replace(/\\/g, '/')}';

function escapeHtml(s) {
  return s.replace(/&/g, '&amp;').replace(/"/g, '&quot;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
}

const inputs = [];
console.log('Found ' + COST_SEO_PAGES.length + ' llm-cost SEO pages');

for (const page of COST_SEO_PAGES) {
  const dir = join(OUTPUT_DIR, page.slug);
  mkdirSync(dir, { recursive: true });

  const faqByPageType = {
    calculator: { q: 'How do I calculate my monthly LLM API costs?', a: 'Enter your token volume, calls per day, and cache hit rate into the cost calculator at ' + BASE_URL + '/llm-pricing/' + page.slug + '/. It estimates monthly spending across 20+ models and shows a side-by-side cost breakdown.' },
    comparison: { q: 'Which LLM API is the cheapest in 2026?', a: 'DeepSeek V4 Flash is the cheapest at $0.14/1M input, followed by Gemini 2.0 Flash Lite. See the full comparison table at ' + BASE_URL + '/llm-pricing/' + page.slug + '/ — pricing and value vary by use case.' },
    optimization: { q: 'How to reduce LLM API costs by 60%?', a: 'Enable prompt caching, route simple queries to cheap models, reduce output tokens, and use batch APIs. See all 7 strategies with real cost numbers at ' + BASE_URL + '/llm-pricing/' + page.slug + '/' },
  };
  const defaultFaq = faqByPageType.optimization;
  const faqItem = faqByPageType[page.pageType] || defaultFaq;

  const html = \`<!doctype html>
<html lang="en">
  <head>
    <meta charset="UTF-8" />
    <link rel="icon" type="image/svg+xml" href="/favicon.svg" />
    <meta name="viewport" content="width=device-width, initial-scale=1.0" />
    <title>\${escapeHtml(page.title)}</title>
    <meta name="description" content="\${escapeHtml(page.description)}" />
    <meta name="keywords" content="\${escapeHtml(page.keywords.join(', '))}" />
    <link rel="canonical" href="\${BASE_URL}/llm-pricing/\${page.slug}/" />
    <meta property="og:title" content="\${escapeHtml(page.title)}" />
    <meta property="og:description" content="\${escapeHtml(page.description)}" />
    <meta property="og:type" content="article" />
    <meta property="og:url" content="\${BASE_URL}/llm-pricing/\${page.slug}/" />
    <meta property="og:image" content="\${BASE_URL}/og-image.png" />
    <meta name="twitter:card" content="summary_large_image" />
    <meta name="twitter:image" content="\${BASE_URL}/og-image.png" />
    <meta name="twitter:title" content="\${escapeHtml(page.title)}" />
    <meta name="twitter:description" content="\${escapeHtml(page.description)}" />
    <link rel="preconnect" href="https://fonts.googleapis.com" />
    <link rel="preconnect" href="https://fonts.gstatic.com" crossorigin />
    <link href="https://fonts.googleapis.com/css2?family=Inter:wght@400;500;600;700&family=JetBrains+Mono:wght@400;500;600;700&display=swap" rel="stylesheet" />
    <script type="application/ld+json">
    {
      "@context": "https://schema.org",
      "@type": "FAQPage",
      	"mainEntity": [{"@type":"Question","name":"\${faqItem.q}","acceptedAnswer":{"@type":"Answer","text":"\${faqItem.a}"}}]
    }
    </script>
  </head>
  <body>
    <script type="application/json" id="seo-data">\${JSON.stringify(page)}</script>
    <div id="root"></div>
    <script type="module" src="/src/llm-pricing/cost-main.tsx"></script>
  </body>
</html>\`;

  writeFileSync(join(dir, 'index.html'), html);
  inputs.push("  'cost-lt-" + page.slug + "': resolve('" + SCRIPTS_DIR + "/..', 'llm-pricing/" + page.slug + "/index.html')");
}

const fragment = \`// Auto-generated by scripts/generate-llm-cost-html.cjs
// Do not edit manually. Run: node scripts/generate-llm-cost-html.cjs
const { resolve } = require('path')
module.exports = {
\${inputs.join(',\\n')}
}
\`;
writeFileSync(join(SCRIPTS_DIR, 'vite-llm-cost-inputs.cjs'), fragment);
console.log('Generated ' + COST_SEO_PAGES.length + ' llm-cost HTML files');
console.log('Wrote vite inputs to scripts/vite-llm-cost-inputs.cjs');
`)

try {
  execSync(`npx tsx "${TMP_FILE}"`, { stdio: 'inherit', cwd: path.join(__dirname, '..') })
} finally {
  fs.unlinkSync(TMP_FILE)
}
