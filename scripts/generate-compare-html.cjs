const fs = require('fs')
const path = require('path')
const { execSync } = require('child_process')

const BASE_URL = 'https://aicalc.cloud'
const OUTPUT_DIR = path.join(__dirname, '..', 'compare')
const TMP_FILE = path.join(__dirname, '_tmp_cmp_data.mjs')

const dataFile = path.join(__dirname, '..', 'src', 'compare', 'seo', 'compare-data.ts')

fs.writeFileSync(TMP_FILE, `
import { COMPARE_PAGES } from '${dataFile.replace(/\\/g, '/')}';
import { writeFileSync, mkdirSync } from 'fs';
import { join, resolve } from 'path';

const OUTPUT_DIR = '${OUTPUT_DIR.replace(/\\/g, '/')}';
const BASE_URL = '${BASE_URL}';
const SCRIPTS_DIR = '${__dirname.replace(/\\/g, '/')}';

function escapeHtml(s) {
  return s.replace(/&/g, '&amp;').replace(/"/g, '&quot;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
}

const inputs = [];
console.log('Found ' + COMPARE_PAGES.length + ' compare pages');

for (const page of COMPARE_PAGES) {
  const dir = join(OUTPUT_DIR, page.slug);
  mkdirSync(dir, { recursive: true });

  const faqSchema = page.faq.map(f => ({
    "@type": "Question",
    "name": f.q,
    "acceptedAnswer": { "@type": "Answer", "text": f.a }
  }));

  const compareSchema = {
    "@context": "https://schema.org",
    "@type": "ItemList",
    "name": page.productA.name + " vs " + page.productB.name,
    "description": page.description,
    "itemListElement": [
      {
        "@type": "ListItem",
        "position": 1,
        "item": {
          "@type": "SoftwareApplication",
          "name": page.productA.name,
          "description": page.productA.tagline,
          "url": page.productA.url,
          "license": page.productA.license,
          "applicationCategory": "DeveloperApplication"
        }
      },
      {
        "@type": "ListItem",
        "position": 2,
        "item": {
          "@type": "SoftwareApplication",
          "name": page.productB.name,
          "description": page.productB.tagline,
          "url": page.productB.url,
          "license": page.productB.license,
          "applicationCategory": "DeveloperApplication"
        }
      }
    ]
  };

  const breadcrumbSchema = {
    "@context": "https://schema.org",
    "@type": "BreadcrumbList",
    "itemListElement": [
      { "@type": "ListItem", "position": 1, "name": "Home", "item": BASE_URL },
      { "@type": "ListItem", "position": 2, "name": "Compare", "item": BASE_URL + "/compare/" },
      { "@type": "ListItem", "position": 3, "name": page.productA.name + " vs " + page.productB.name, "item": BASE_URL + "/compare/" + page.slug + "/" },
    ]
  };

  const html = \`<!doctype html>
<html lang="en">
  <head>
    <meta charset="UTF-8" />
    <link rel="icon" type="image/svg+xml" href="/favicon.svg" />
    <meta name="viewport" content="width=device-width, initial-scale=1.0" />
    <title>\${escapeHtml(page.title)}</title>
    <meta name="description" content="\${escapeHtml(page.description)}" />
    <meta name="keywords" content="\${escapeHtml(page.keywords.join(', '))}" />
    <link rel="canonical" href="\${BASE_URL}/compare/\${page.slug}/" />
    <meta property="og:title" content="\${escapeHtml(page.title)}" />
    <meta property="og:description" content="\${escapeHtml(page.description)}" />
    <meta property="og:type" content="article" />
    <meta property="og:url" content="\${BASE_URL}/compare/\${page.slug}/" />
    <meta property="og:image" content="\${BASE_URL}/og-image.png" />
    <meta name="twitter:card" content="summary_large_image" />
    <meta name="twitter:image" content="\${BASE_URL}/og-image.png" />
    <meta name="twitter:title" content="\${escapeHtml(page.title)}" />
    <meta name="twitter:description" content="\${escapeHtml(page.description)}" />
    <link rel="preconnect" href="https://fonts.googleapis.com" />
    <link rel="preconnect" href="https://fonts.gstatic.com" crossorigin />
    <link href="https://fonts.googleapis.com/css2?family=Inter:wght@400;500;600;700&family=JetBrains+Mono:wght@400;500;600;700&display=swap" rel="stylesheet" />
    <script type="application/ld+json">
    {
      "@context": "https://schema.org",
      "@type": "FAQPage",
      "mainEntity": \${JSON.stringify(faqSchema)}
    }
    </script>
    <script type="application/ld+json">
    \${JSON.stringify(compareSchema)}
    </script>
    <script type="application/ld+json">
    \${JSON.stringify(breadcrumbSchema)}
    </script>
  </head>
  <body>
    <script type="application/json" id="seo-data">\${JSON.stringify(page)}</script>
    <div id="root"></div>
    <script type="module" src="/src/compare/main.tsx"></script>
  </body>
</html>\`;

  writeFileSync(join(dir, 'index.html'), html);
  inputs.push("  'cmp-lt-" + page.slug + "': resolve('" + SCRIPTS_DIR + "/..', 'compare/" + page.slug + "/index.html')");
}

const fragment = \`// Auto-generated by scripts/generate-compare-html.cjs
// Do not edit manually. Run: node scripts/generate-compare-html.cjs
const { resolve } = require('path')
module.exports = {
\${inputs.join(',\\n')}
}
\`;
writeFileSync(join(SCRIPTS_DIR, 'vite-compare-inputs.cjs'), fragment);
console.log('Generated ' + COMPARE_PAGES.length + ' compare HTML files');
console.log('Wrote vite inputs to scripts/vite-compare-inputs.cjs');
`)

try {
  execSync(`npx tsx "${TMP_FILE}"`, { stdio: 'inherit', cwd: path.join(__dirname, '..') })
} finally {
  fs.unlinkSync(TMP_FILE)
}
