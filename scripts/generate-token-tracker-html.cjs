const fs = require('fs')
const path = require('path')
const { execSync } = require('child_process')

const BASE_URL = 'https://aicalc.cloud'
const OUTPUT_DIR = path.join(__dirname, '..', 'token-tracker')
const TMP_FILE = path.join(__dirname, '_tmp_tracker_data.mjs')

const dataFile = path.join(__dirname, '..', 'src', 'token-tracker', 'seo', 'scene-data.ts')

fs.writeFileSync(TMP_FILE, `
import { SCENE_PAGES } from '${dataFile.replace(/\\/g, '/')}';
import { writeFileSync, mkdirSync } from 'fs';
import { join, resolve } from 'path';

const OUTPUT_DIR = '${OUTPUT_DIR.replace(/\\/g, '/')}';
const BASE_URL = '${BASE_URL}';
const SCRIPTS_DIR = '${__dirname.replace(/\\/g, '/')}';

function escapeHtml(s) {
  return s.replace(/&/g, '&amp;').replace(/"/g, '&quot;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
}

const inputs = [];
console.log('Found ' + SCENE_PAGES.length + ' scene pages');

for (const page of SCENE_PAGES) {
  const dir = join(OUTPUT_DIR, page.slug);
  mkdirSync(dir, { recursive: true });

  const seoData = JSON.stringify({
    scene: page.scene,
    h1: page.h1,
    defaultPrompt: page.defaultPrompt,
    defaultOutputTokens: page.defaultOutputTokens,
    defaultCallsPerMonth: page.defaultCallsPerMonth,
  });

  const faqSchema = [
    { "@type": "Question", "name": "How much does an AI " + page.scene + " cost per month?", "acceptedAnswer": { "@type": "Answer", "text": "Costs vary by model and usage. Use this calculator to get exact estimates for your specific use case." } },
    { "@type": "Question", "name": "Which AI model is cheapest for " + page.scene + "?", "acceptedAnswer": { "@type": "Answer", "text": "GPT-4o Mini and Gemini Flash are typically the cheapest options. Check the comparison table for exact pricing." } },
  ];

  const html = \`<!doctype html>
<html lang="en">
  <head>
    <meta charset="UTF-8" />
    <link rel="icon" type="image/svg+xml" href="/favicon.svg" />
    <meta name="viewport" content="width=device-width, initial-scale=1.0" />
    <title>\${escapeHtml(page.title)}</title>
    <meta name="description" content="\${escapeHtml(page.description)}" />
    <meta name="keywords" content="\${escapeHtml(page.keywords.join(', '))}" />
    <link rel="canonical" href="\${BASE_URL}/token-tracker/\${page.slug}/" />
    <meta property="og:title" content="\${escapeHtml(page.title)}" />
    <meta property="og:description" content="\${escapeHtml(page.description)}" />
    <meta property="og:type" content="article" />
    <meta property="og:url" content="\${BASE_URL}/token-tracker/\${page.slug}/" />
    <meta property="og:image" content="\${BASE_URL}/og-image.png" />
    <meta name="twitter:card" content="summary_large_image" />
    <meta name="twitter:image" content="\${BASE_URL}/og-image.png" />
    <meta name="twitter:title" content="\${escapeHtml(page.title)}" />
    <meta name="twitter:description" content="\${escapeHtml(page.description)}" />
    <link rel="preconnect" href="https://fonts.googleapis.com" />
    <link rel="preconnect" href="https://fonts.gstatic.com" crossorigin />
    <link href="https://fonts.googleapis.com/css2?family=Inter:wght@400;500;600;700&family=JetBrains+Mono:wght@400;500;600;700&display=swap" rel="stylesheet" />
    <script type="application/ld+json">
    {
      "@context": "https://schema.org",
      "@type": "FAQPage",
      "mainEntity": \${JSON.stringify(faqSchema)}
    }
    </script>
  </head>
  <body>
    <script type="application/json" id="seo-data">\${seoData}</script>
    <div id="root"></div>
    <script type="module" src="/src/token-tracker/main.tsx"></script>
  </body>
</html>\`;

  writeFileSync(join(dir, 'index.html'), html);
  inputs.push("  'tracker-lt-" + page.slug + "': resolve('" + SCRIPTS_DIR + "/..', 'token-tracker/" + page.slug + "/index.html')");
}

const fragment = \`// Auto-generated by scripts/generate-token-tracker-html.cjs
// Do not edit manually. Run: node scripts/generate-token-tracker-html.cjs
const { resolve } = require('path')
module.exports = {
\${inputs.join(',\\n')}
}
\`;
writeFileSync(join(SCRIPTS_DIR, 'vite-tracker-inputs.cjs'), fragment);
console.log('Generated ' + SCENE_PAGES.length + ' scene HTML files');
console.log('Wrote vite inputs to scripts/vite-tracker-inputs.cjs');
`)

try {
  execSync(`npx tsx "${TMP_FILE}"`, { stdio: 'inherit', cwd: path.join(__dirname, '..') })
} finally {
  fs.unlinkSync(TMP_FILE)
}
