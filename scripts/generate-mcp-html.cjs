// scripts/generate-mcp-html.cjs
// Pre-render 10 MCP server detail pages — one per server in src/mcp/seo/mcp-data.ts
// Outputs /mcp-servers/<slug>/index.html

const fs = require('fs')
const path = require('path')
const { execSync } = require('child_process')

const BASE_URL = 'https://aicalc.cloud'
const OUTPUT_DIR = path.join(__dirname, '..', 'mcp-servers')
const TMP_FILE = path.join(__dirname, '_tmp_mcp_data.mjs')

const dataFile = path.join(__dirname, '..', 'src', 'mcp', 'seo', 'mcp-data.ts')

fs.writeFileSync(
  TMP_FILE,
  `
import { MCP_SERVERS, CATEGORY_LABELS } from '${dataFile.replace(/\\/g, '/')}';
import { writeFileSync, mkdirSync } from 'fs';
import { join } from 'path';

const OUTPUT_DIR = '${OUTPUT_DIR.replace(/\\/g, '/')}';
const BASE_URL = '${BASE_URL}';
const SCRIPTS_DIR = '${__dirname.replace(/\\/g, '/')}';

function escapeHtml(s) {
  return String(s).replace(/&/g, '&amp;').replace(/"/g, '&quot;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
}

const inputs = [];
console.log('Generating ' + MCP_SERVERS.length + ' MCP detail pages');

for (const s of MCP_SERVERS) {
  const dir = join(OUTPUT_DIR, s.slug);
  mkdirSync(dir, { recursive: true });

  const title = s.name + ' MCP Server — 1 Command Install for Claude & Cursor | codehelper';
  const desc = 'Connect ' + s.name + ' to Claude or Cursor in 30 seconds. ' + s.oneLiner + ' Free, open source, with config examples.';
  const keywords = [
    s.name.toLowerCase() + ' mcp server',
    s.name.toLowerCase() + ' mcp',
    'claude desktop ' + s.name.toLowerCase() + ' mcp',
    'cursor ' + s.name.toLowerCase() + ' mcp',
    s.slug + ' mcp install',
    s.slug + ' mcp ' + s.name.toLowerCase(),
  ].join(', ');

  const faqSchema = [
    { "@type": "Question", "name": "How do I install the " + s.name + " MCP server?", "acceptedAnswer": { "@type": "Answer", "text": "Run: " + s.installCommand + ". Then paste the config JSON shown on this page into your Claude Desktop or Cursor config file." } },
    { "@type": "Question", "name": "When should I use " + s.name + " MCP?", "acceptedAnswer": { "@type": "Answer", "text": s.useCase[0] + ' Other typical scenarios: ' + s.useCase.slice(1).join('; ') + '.' } },
    { "@type": "Question", "name": "What environment variables does " + s.name + " MCP need?", "acceptedAnswer": { "@type": "Answer", "text": s.envVars.length > 0 ? s.envVars.map(v => v.key + ' (' + (v.required ? 'required' : 'optional') + '): ' + v.note).join('; ') : 'None — runs out of the box.' } }
  ];

  const softwareSchema = {
    "@context": "https://schema.org",
    "@type": "SoftwareApplication",
    "name": s.name + ' MCP Server',
    "description": s.description,
    "url": BASE_URL + '/mcp-servers/' + s.slug + '/',
    "applicationCategory": "DeveloperApplication",
    "operatingSystem": "Any",
    "offers": { "@type": "Offer", "price": "0", "priceCurrency": "USD" }
  };

  const html = \`<!doctype html>
<html lang="en">
  <head>
    <meta charset="UTF-8" />
    <link rel="icon" type="image/svg+xml" href="/favicon.svg" />
    <meta name="viewport" content="width=device-width, initial-scale=1.0" />
    <title>\${escapeHtml(title)}</title>
    <meta name="description" content="\${escapeHtml(desc)}" />
    <meta name="keywords" content="\${escapeHtml(keywords)}" />
    <link rel="canonical" href="\${BASE_URL}/mcp-servers/\${s.slug}/" />
    <meta property="og:title" content="\${escapeHtml(title)}" />
    <meta property="og:description" content="\${escapeHtml(desc)}" />
    <meta property="og:type" content="article" />
    <meta property="og:url" content="\${BASE_URL}/mcp-servers/\${s.slug}/" />
    <meta property="og:image" content="\${BASE_URL}/og-image.png" />
    <meta name="twitter:card" content="summary_large_image" />
    <meta name="twitter:image" content="\${BASE_URL}/og-image.png" />
    <meta name="twitter:title" content="\${escapeHtml(title)}" />
    <meta name="twitter:description" content="\${escapeHtml(desc)}" />
    <link rel="preconnect" href="https://fonts.googleapis.com" />
    <link rel="preconnect" href="https://fonts.gstatic.com" crossorigin />
    <link href="https://fonts.googleapis.com/css2?family=Inter:wght@400;500;600;700&family=JetBrains+Mono:wght@400;500;600;700&display=swap" rel="stylesheet" />
    <script type="application/ld+json">
    {
      "@context": "https://schema.org",
      "@type": "FAQPage",
      "mainEntity": \${JSON.stringify(faqSchema)}
    }
    </script>
    <script type="application/ld+json">
    \${JSON.stringify(softwareSchema)}
    </script>
  </head>
  <body>
    <script type="application/json" id="seo-data">\${JSON.stringify(s)}</script>
    <div id="root"></div>
    <script type="module" src="/src/mcp/detail-main.tsx"></script>
  </body>
</html>\`;

  writeFileSync(join(dir, 'index.html'), html);
  inputs.push("  'mcp-d-" + s.slug + "': resolve('" + SCRIPTS_DIR + "/..', 'mcp-servers/" + s.slug + "/index.html')");
}

const fragment = \`// Auto-generated by scripts/generate-mcp-html.cjs
const { resolve } = require('path')
module.exports = {
\${inputs.join(',\\n')}
}
\`;
writeFileSync(join(SCRIPTS_DIR, 'vite-mcp-inputs.cjs'), fragment);
console.log('Wrote vite inputs to scripts/vite-mcp-inputs.cjs');
`
)

try {
  execSync(`npx tsx "${TMP_FILE}"`, { stdio: 'inherit', cwd: path.join(__dirname, '..') })
} finally {
  fs.unlinkSync(TMP_FILE)
}
